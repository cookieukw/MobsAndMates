export function randomVillagerName() {
  const start = syllablesStart[(Math.random() * syllablesStart.length) | 0];
  const middle = syllablesMiddle[(Math.random() * syllablesMiddle.length) | 0];
  const end = syllablesEnd[(Math.random() * syllablesEnd.length) | 0];

  let name = start + middle + end;

  if (Math.random() < 0.45) {
    const last = surnames[(Math.random() * surnames.length) | 0];
    name += " " + last;
  }

  return name;
}
